package com.vidvaan.corejava.basic004incrementORdecrementoperator;

public class Example02Decrement {
	public static void main(String[] args) {
		int i = 10;
		i--;

		// prints 9
		System.out.println(i);
		--i;

		// prints 8
		System.out.println(i);

		// prints 7
		System.out.println(--i);

		// prints 7
		System.out.println(i--);

		// prints 6
		System.out.println(i);
	}
}
