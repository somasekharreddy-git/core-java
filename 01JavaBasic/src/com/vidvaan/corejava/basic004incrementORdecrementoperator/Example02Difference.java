package com.vidvaan.corejava.basic004incrementORdecrementoperator;

public class Example02Difference {
	public static void main(String args[]) {
		int n = 10;
		System.out.println(n);
		System.out.println(n++);
		System.out.println(n);

		int m = 10;
		System.out.println(m);
		System.out.println(++m);
		System.out.println(m);
	}

}
