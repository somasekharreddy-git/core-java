package com.vidvaan.corejava.basic003relationaloperator;

class RelationalOperatorDemo2 {

	public static void main(String[] args) {
		int ivar1 = 1;
		int ivar2 = 2;

		if (ivar1 == ivar2)
			System.out.println("ivar1 == ivar2");

		if (ivar1 != ivar2)
			System.out.println("ivar1 != ivar2");

		if (ivar1 > ivar2)
			System.out.println("ivar1 > ivar2");

		if (ivar1 < ivar2)
			System.out.println("ivar1 < ivar2");

		if (ivar1 <= ivar2)
			System.out.println("ivar1 <= ivar2");
	}
}