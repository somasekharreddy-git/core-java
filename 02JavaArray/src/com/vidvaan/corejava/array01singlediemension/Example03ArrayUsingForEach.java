package com.vidvaan.corejava.array01singlediemension;

public class Example03ArrayUsingForEach {
	public static void main(String[] args) {

		// creating char array and storing values
		char[] chars = { 'v', 'i', 'd', 'v', 'a', 'a', 'n' };

		// accessing the values from char array

		for (char c : chars) {
			System.out.println(c);
		}

	}
}
